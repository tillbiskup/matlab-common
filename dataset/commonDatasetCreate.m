function dataset = commonDatasetCreate(varargin)
% COMMONDATASETCREATE Create and return data structure of most general
% dataset.
%
% Usage
%   dataset = commonDatasetCreate
%   dataset = commonDatasetCreate(<parameters>)
%
%   dataset    - struct
%                Structure complying with the data structure of the dataset
%                of the common toolbox
%
%   parameters - key-value pairs (OPTIONAL)
%
%                Optional parameters may include:
%
%                numberOfAxes      - scalar
%                                    Number of axes the dataset should have
%                                    Default: 2
%
%                hasOptionalFields - logical
%                                    Should  the dataset have optional
%                                    fields?
%                                    Default: false
%    
% Hint: Parameters can be provided as a structure with the fieldnames
% corresponding to the parameter names specified above.
%
% SEE ALSO: commonHistoryCreate

% Copyright (c) 2014-15, Till Biskup
% 2015-06-22

% Assign output parameter
dataset = struct();

% Parse input arguments using the inputParser functionality
p = inputParser;            % Create an instance of the inputParser class.
p.FunctionName = mfilename; % Function name to be included in error messages
p.KeepUnmatched = true;     % Enable errors on unmatched arguments
p.StructExpand = true;      % Enable passing arguments in a structure
% Add optional parameters, with default values
p.addParamValue('numberOfAxes',2,@isscalar);
p.addParamValue('hasOptionalFields',false,@islogical);
% Parse input arguments
p.parse(varargin{:});

% Define version of dataset structure
structureVersion = '0.4';

dataset.data = [];
dataset.origdata = [];

for axis = 1:p.Results.numberOfAxes
    dataset.axes.data(axis) = axisStructureCreate;
    dataset.axes.origdata(axis) = axisStructureCreate;
end

dataset.parameters = struct(...
    'operator','',...
    'date',struct(...
        'start','',...
        'end',''...
        ),...
    'temperature',struct(...
        'value',[],...
        'unit','' ...
        ) ...
    );
% NOTE: Matlab doesn't handle cells defined in structs together with other
%       parameters. Therefore, you have to add them explicitly afterwards.
dataset.parameters.purpose = cell(0);

dataset.sample = struct(...
    'name','' ...
    );
dataset.sample.description = cell(0);
dataset.sample.solvent = cell(0);
dataset.sample.preparation = cell(0);

dataset.comment = cell(0);
dataset.history = cell(0);
dataset.file = struct(...
    'name','', ...
    'format','' ...
    );
dataset.format = struct(...
    'type','common dataset',...
    'version',structureVersion ...
    );
dataset.label = '';

dataset.display = struct(...
    'position',struct(...
        'data',ones(1,p.Results.numberOfAxes)...
        ),...
    'lines',struct(...
        'data',struct(...
            'color','',...
            'style','',...
            'marker','',...
            'width',''...
            )...
        ),...
    'scaling',struct(...
        'data',ones(1,p.Results.numberOfAxes+1)...
        ),...
    'smoothing',struct(...
        'data',struct(...
            'value',[],...
            'filterFunction',''...
            )...
        )...
    );

if p.Results.hasOptionalFields
    dataset.calculated = [];
    dataset.calculation = struct(...
        'direction',1,...
        'position',1,...
        'scaling',1,...
        'infoStructure','' ...
        ); 
    dataset.display.position.calculated  = ones(1,p.Results.numberOfAxes);
    dataset.display.lines.calculated = struct(...
        'color','',...
        'style','',...
        'marker','',...
        'width',''...
        );
    dataset.display.scaling.calculated  = ones(1,p.Results.numberOfAxes+1);
    dataset.display.smoothing.calculated = struct(...
        'value',[],...
        'filterFunction',''...
        );
    for axis = 1:p.Results.numberOfAxes
        dataset.axes.calculated(axis) = axisStructureCreate;
    end
end

end

function axis = axisStructureCreate

axis = struct(...
    'values',[],...
    'measure','',...
    'unit','' ...
    );

end
